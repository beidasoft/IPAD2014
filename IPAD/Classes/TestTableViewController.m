- (void)viewDidLoad {
    [super viewDidLoad];
	//设定抬头名称
    
	self.navigationItem.title = @"其它资料";
	
    //获取pdf文件的路径
	NSArray *paths = NSSearchPathForDirectoriesInDomains(NSDocumentDirectory, NSUserDomainMask, YES);
	NSString *documentsDirectory = [paths objectAtIndex:0];
	NSString *docPath = [documentsDirectory stringByAppendingString:@"/pdf"];
	
	NSFileManager *fileManager = [[NSFileManager alloc] init];
	array = [[NSArray alloc] init];
    //将文件内容读取到数组中
	array =  [[fileManager contentsOfDirectoryAtPath:docPath error:nil]copy];
	
    [fileManager release];

}


